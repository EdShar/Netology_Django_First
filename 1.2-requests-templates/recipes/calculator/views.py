def home_view(request):
    template_name = 'calculator/index.html'

    pages = {
        'Омлет': reverse('omlet'),
        'Паста': reverse('pasta'),
        'Бутерброд': reverse('buter')
    }

    context = {
        'recipe': pages
    }
    return render(request, template_name, context)


